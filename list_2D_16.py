#2D lists = a list of lists

strategy = ["Clash Of Clans","Chess"]
multiplayer = ["Valorant","Fortnite","PUBG","League Of Legends"]
free = ["GTA","Assassins","RDR"]

games = [strategy,multiplayer,free]

print(games) #displays all lists
print(games[1]) #displays the list with index 1
print(games[1][1]) #displays the data from the list with index 1 having index 1