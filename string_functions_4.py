#string methods/function

name = "ragosh Shrestha"
print(len(name)) #length of the string
print(name.find("S")) #find the index of the character from the string
print(name.capitalize()) #capitalize the string first letter
print(name.upper()) #changes into upper case
print(name.lower()) #changes into lower case
print(name.isdigit()) #True if string is a digit
print(name.isalpha()) #True if string is alphabet
print(name.count("a")) #counts the character inside the string
print(name.replace("a","u")) #replaces a character with another character
print(name*4) #displays the string for desired no of time
